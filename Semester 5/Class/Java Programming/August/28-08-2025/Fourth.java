// Sum of 5 numbers

class Fourth{
    public static void main(String args[]){
        int sum = 0;
        int n1 = 10;
        int n2 = 20;
        int n3 = 30;
        int n4 = 40;
        int n5 = 50;
        sum = n1 + n2 + n3 + n4 + n5;
        System.out.println("The sum is: " + sum);
        System.out.println("The average is: " + (sum / 5));
    }
}